user_input = int(input('Type a number:\n'))

prime = True
divisors = 0

for n in range(2, user_input):
    divisors
    if user_input % n == 0:
        divisors += 1
        prime = False
    elif divisors > 2:
        prime = True

if prime:
    divisors
    divisors = 2

print(prime)
print(divisors)

if divisors > 2:
    print('Not a prime number.')
else:
    print('It\'s a prime number')
    print(f'Divisors: {divisors}')
